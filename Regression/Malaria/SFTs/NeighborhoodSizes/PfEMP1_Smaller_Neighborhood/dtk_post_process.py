def application(output_folder="output", insetchart="InsetChart.json",
                insetchart_reference="InsetChart_reference.json",
                config_filename="config.json",
                report_name=dtk_sft.sft_output_filename,
                debug=False):
    if debug:
        print("output_folder: " + output_folder)
        print("insetchart: " + insetchart + "\n")
        print("insetchart_reference: " + insetchart_reference + "\n")
        print("config_filename: " + config_filename + "\n")
        print("report_name: " + report_name + "\n")
        print("debug: " + str(debug) + "\n")

    dtk_sft.wait_for_done()
    param_obj = mal_sup.load_genetics_parameters(config_filename, debug)
    insetchart_data = mal_sup.load_neighborhood_insetchart(output_folder=output_folder, insetchart_filename=insetchart, debug=debug)
    insetchart_data_reference = reference()
    mal_sup.create_neighborhood_report_file(insetchart_data, "lower", insetchart_data_reference, param_obj, report_name, debug)


